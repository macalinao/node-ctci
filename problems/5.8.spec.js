var expect = require('chai').expect;
var drawHorizontalLine = require('./5.8');

function newScreen() {
  return [
    [0x00, 0x00, 0x00, 0x00, 0x00],
    [0x00, 0x00, 0x00, 0x00, 0x00],
    [0x00, 0x00, 0x00, 0x00, 0x00],
    [0x00, 0x00, 0x00, 0x00, 0x00],
    [0x00, 0x00, 0x00, 0x00, 0x00],
    [0x00, 0x00, 0x00, 0x00, 0x00]
  ];
}

describe('5.8', function() {
  it('should draw on the screen properly', function() {

    var expected = [
      [0x00, 0x00, 0x00, 0x00, 0x00],
      [0x00, 0x00, 0x00, 0x00, 0x00],
      [0x00, 0x0f, 0xff, 0xff, 0xf0],
      [0x00, 0x00, 0x00, 0x00, 0x00],
      [0x00, 0x00, 0x00, 0x00, 0x00],
      [0x00, 0x00, 0x00, 0x00, 0x00]
    ];

    expect(drawHorizontalLine(newScreen(), 40, 12, 35, 2)).to.eql(expected);
  });

  it('should draw on the screen if only bytes', function() {

    var expected = [
      [0x00, 0x00, 0x00, 0x00, 0x00],
      [0x00, 0x00, 0x00, 0x00, 0x00],
      [0x00, 0x00, 0xff, 0xff, 0x00],
      [0x00, 0x00, 0x00, 0x00, 0x00],
      [0x00, 0x00, 0x00, 0x00, 0x00],
      [0x00, 0x00, 0x00, 0x00, 0x00]
    ];

    expect(drawHorizontalLine(newScreen(), 40, 16, 31, 2)).to.eql(expected);
  });

  it('should draw on the screen if only extra on left', function() {

    var expected = [
      [0x00, 0x00, 0x00, 0x00, 0x00],
      [0x00, 0x00, 0x00, 0x00, 0x00],
      [0x00, 0x01, 0xff, 0xff, 0x00],
      [0x00, 0x00, 0x00, 0x00, 0x00],
      [0x00, 0x00, 0x00, 0x00, 0x00],
      [0x00, 0x00, 0x00, 0x00, 0x00]
    ];

    expect(drawHorizontalLine(newScreen(), 40, 15, 31, 2)).to.eql(expected);
  });

  it('should draw on the screen if only extra on right', function() {

    var expected = [
      [0x00, 0x00, 0x00, 0x00, 0x00],
      [0x00, 0x00, 0x00, 0x00, 0x00],
      [0x00, 0x00, 0xff, 0xff, 0x80],
      [0x00, 0x00, 0x00, 0x00, 0x00],
      [0x00, 0x00, 0x00, 0x00, 0x00],
      [0x00, 0x00, 0x00, 0x00, 0x00]
    ];

    expect(drawHorizontalLine(newScreen(), 40, 16, 32, 2)).to.eql(expected);
  });
});
